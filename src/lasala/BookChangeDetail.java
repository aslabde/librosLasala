    public void setFields(ArrayList<String> bookParsedResults){
       
               
        this.jTextField1.setText(bookParsedResults.get(0));
        this.jTextField2.setText(bookParsedResults.get(1));
        this.jTextField3.setText(bookParsedResults.get(2));
        this.jTextField4.setText(bookParsedResults.get(3));
        this.jTextField5.setText(bookParsedResults.get(4));
        this.jTextField6.setText(bookParsedResults.get(5));
        
        this.buttonGroup1.add(this.jRadioButton1);
        this.buttonGroup1.add(this.jRadioButton2);
        this.buttonGroup1.add(this.jRadioButton3);
        String s =(bookParsedResults.get(6));
        
        switch(s){
            case "AVAILABLE":  this.jRadioButton1.setSelected(true);
                                   break;
            case "RETURNED":  this.jRadioButton2.setSelected(true);
                                   break;
            case "SOLD_OUT":  this.jRadioButton3.setSelected(true);
                                   break;    
                
        }
    }
                       
        
    public boolean isNumerical(char c){
    
  return(Character.isDigit(c)  ||  c==KeyEvent.VK_DELETE  ||  c==KeyEvent.VK_BACK_SPACE 
            ||c==KeyEvent.VK_COMMA || c==KeyEvent.VK_TAB || c==KeyEvent.VK_PERIOD );
        
    }
